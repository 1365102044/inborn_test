#import "YBLikeAndRecordBaseModel.h"

@implementation YBLikeAndRecordBaseModel
-(void)setData:(NSArray *)data
{
    NSMutableArray *tempmodelarr = [NSMutableArray array];
    
    for (NSDictionary *dict in data) {
        YBLikeAndRecordModel * model = [YBLikeAndRecordModel modelWithDictionary:dict];
        [tempmodelarr addObject:model];
    }
    _dataModelArr = tempmodelarr;
}

@end

@implementation YBLikeAndRecordModel


@end
